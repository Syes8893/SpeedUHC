package me.syes.speeduhc.speeduhcplayer;

import java.util.HashMap;
import java.util.UUID;

import org.bukkit.Bukkit;
import org.bukkit.configuration.file.FileConfiguration;

import me.syes.speeduhc.SpeedUHC;
import me.syes.speeduhc.arena.Arena;
import me.syes.speeduhc.game.Game;
import me.syes.speeduhc.perk.Perk;

public class SpeedUHCPlayer {
	
	private UUID uuid;

	private int gameKills;
	
	private int elo;
	private int wins;
	private int kills;
	private int deaths;
	private int coins;
	
	private HashMap<Perk, Integer> perkLevels;
	
	private Arena editedArena;
	
	public SpeedUHCPlayer(UUID uuid) {
		this.uuid = uuid;
		this.elo = 0;
		this.wins = 0;
		this.kills = 0;
		this.deaths = 0;
		this.coins = 0;
		
		this.perkLevels = new HashMap<Perk, Integer>();
		for(Perk p : SpeedUHC.getInstance().getPerkManager().getPerks())
			this.perkLevels.put(p, 0);
		
		this.gameKills = 0;
		SpeedUHC.getInstance().getSpeedUHCPlayerManager().addUhcPlayer(this.uuid, this);
	}

	public void loadStats(FileConfiguration statsFile) {
		this.elo = statsFile.getInt("ELO");
		this.wins = statsFile.getInt("Wins");
		this.kills = statsFile.getInt("Kills");
		this.deaths = statsFile.getInt("Deaths");
		this.coins = statsFile.getInt("Coins");
		for(String str : statsFile.getConfigurationSection("Perks").getKeys(false))
			this.perkLevels.put(SpeedUHC.getInstance().getPerkManager().getPerk(str)
					, statsFile.getConfigurationSection("Perks").getInt(str));
	}

	//Player Stats
	public UUID getUuid() {
		return uuid;
	}
	
	public int getElo() {
		return elo;
	}
	
	public void setElo(int elo) {
		this.elo = elo;
	}
	
	public void addElo(int elo) {
		this.elo += elo;
	}
	
	public void removeElo(int elo) {
		if(this.elo - elo <= 0) {
			this.elo = 0;
			return;
		}
		this.elo += -elo;
	}
	
	public int getWins() {
		return this.wins;
	}
	
	public void addWin() {
		this.wins += 1;
	}
	
	public int getKills() {
		return this.kills;
	}
	
	public void addKill() {
		this.kills += 1;
	}
	
	public int getDeaths() {
		return this.deaths;
	}
	
	public void addDeath() {
		this.deaths += 1;
	}
	
	public int getCoins() {
		return this.coins;
	}
	
	public void addCoins(int coins) {
		this.coins += coins;
	}
	
	public void removeCoins(int coins) {
		this.coins += -coins;
	}
	
	public int getPerkLevel(Perk p) {
		if(!perkLevels.containsKey(p))
			return 0;
		return this.perkLevels.get(p);
	}
	
	public void setPerkLevel(Perk p, int level) {
		this.perkLevels.put(p, level);
	}
	
	
	//Game Stats
	
	public int getGameKills() {
		return this.gameKills;
	}

	public void resetGameKills() {
		this.gameKills = 0;
	}
	
	public void addGameKill() {
		this.gameKills += 1;
		this.kills += 1;
	}
	
	
	//Game Handling Methods
	public boolean isInGame() {
		if(SpeedUHC.getInstance().getSpeedUHCPlayerManager().getUhcPlayerGame(this) == null) 
			return false;
		else 
			return true;
	}

	public Game getGame() {
		return SpeedUHC.getInstance().getSpeedUHCPlayerManager().getUhcPlayerGame(this);
	}

	public void setGame(Game g) {
		SpeedUHC.getInstance().getSpeedUHCPlayerManager().setUhcPlayerGame(this, g);
	}

	public boolean isEditingArena() {
		if(this.editedArena == null)
			return false;
		else 
			return true;
	}

	public Arena getEditedArena() {
		return this.editedArena;
	}

	public void setEditedArena(Arena arena) {
		this.editedArena = arena;
	}

}
